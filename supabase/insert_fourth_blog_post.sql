<p>Wenn du merkst, dass du dich ständig verzettelst, Dinge vergisst oder lange brauchst, um einfachste Aufgaben zu beginnen, arbeitet dein Gehirn bereits im Energiesparmodus.</p>

<p>Das ist kein Mangel an Disziplin, sondern ein Signal: Dein Geist braucht Raum, um zu verarbeiten.</p>

