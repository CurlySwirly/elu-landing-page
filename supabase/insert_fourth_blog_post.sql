<p><em>Versuch, dich nicht zu verurteilen. Emotionen sind keine Schwäche, sondern Ausdruck deines Körpers, dass er Schutz braucht.</em></p>

<h2>5. Dein Essverhalten verändert sich</h2>

<p>Wenig Hunger, ständiges Snacken, Heißhunger auf Süßes – das sind oft unbewusste Kompensationsmechanismen bei Überforderung. Der Körper sucht schnelle Energie, weil er auf „Alarmmodus" läuft.</p>

<p>Beobachte, ohne zu bewerten. Frage dich: Was brauche ich wirklich – Energie oder Entlastung?</p>

<h2>6. Du ziehst dich sozial zurück</h2>

<p>Wenn du keine Lust mehr auf Gespräche hast, obwohl du dich nach Verbindung sehnst, ist das oft ein Zeichen innerer Erschöpfung. Dein Körper will nicht „niemanden sehen", sondern einfach nichts leisten müssen.</p>

<p>Das ist okay. Ruhe bedeutet manchmal, allein zu sein, um wieder Platz für andere zu haben.</p>

<h2>7. Du funktionierst – aber fühlst nichts</h2>

<p>Das ist eines der deutlichsten Zeichen: Wenn du Dinge tust, aber keine Freude mehr spürst. Dein Körper schützt dich, indem er in den Energiesparmodus geht. Doch langfristig führt das zu innerer Leere oder Burnout.</p>

<p><em>Das ist der Moment, um aktiv gegenzusteuern – nicht mit mehr Aktivität, sondern mit bewusstem Innehalten.</em></p>

