<h2>1. Du bist ständig erschöpft – auch nach dem Schlaf</h2>

<p>Wenn du morgens aufwachst und dich trotz acht Stunden Schlaf ausgelaugt fühlst, ist das kein Zeichen von Faulheit, sondern von Überlastung. Dein Nervensystem bekommt zu wenig echte Erholung.</p>

<p>Ruhe bedeutet nicht nur Schlaf, sondern auch geistige Entlastung. Versuche tagsüber kleine Pausen einzubauen – fünf Minuten Atmen, Fenster öffnen, Augen schließen.</p>

